// Char almacena caracteres

#include <stdio.h>

int main(){

    char letra = 'a';
    char calificacion = 'F';
    char simbolo = '?';
    char moneda = '$';
    printf("La letra es %c.\n", letra);
    printf("Tu calificación es %c.\n", calificacion);
    printf("Este es el símbolo %c.\n", simbolo);
    printf("El símbolo de la moneda es %c.\n", moneda);

    return 0;
}